#define ST7789V2_COLMOD_RGB_18bit		(0x6 << 4)
#define ST7789V2_COLMOD_MCU_gray		(0)
#define ST7789V2_COLMOD_MCU_3bit		(1)
#define ST7789V2_COLMOD_MCU_8bit		(2)
#define ST7789V2_COLMOD_MCU_12bit		(3)
#define ST7789V2_COLMOD_MCU_16bit		(5)
#define ST7789V2_COLMOD_MCU_18bit		(6)
#define ST7789V2_COLMOD_MCU_24bit		(7)

#define ST7789V2_CMD_RAMWR_CONTINUE	0x3c
#define ST7789V2_CMD_TESET			0x44
#define ST7789V2_CMD_SCANLINEGET		0x45
#define ST7789V2_CMD_BRIGHTNESS		0x51
