#define ST7789V2_RAMACC_COL_INV		(0x1 << 6)
#define ST7789V2_RAMACC_ROW_INV		(0x1 << 7)

#define ST7789V2_CMD_VERTSCROLL_ADDR	0x37
#define ST7789V2_CMD_IDLEMOD_OFF		0x38
#define ST7789V2_CMD_IDLEMOD_ON		0x39

