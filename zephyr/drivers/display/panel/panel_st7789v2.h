#define ST7789V2_CMD_READSTATUS		0x09
#define ST7789V2_CMD_SLEEP_IN			0x10
#define ST7789V2_CMD_SLEEP_OUT		0x11
#define ST7789V2_CMD_PARTIALMOD		0x12
#define ST7789V2_CMD_NORMALMOD		0x13
