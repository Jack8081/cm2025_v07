#define ST7789V2_CMD_VERTSCROLL		0x33
#define ST7789V2_CMD_TE_OFF			0x34
#define ST7789V2_CMD_TE_ON			0x35

#define ST7789V2_CMD_RAMACC			0x36
#define ST7789V2_RAMACC_HREFR_INV		(0x1 << 2)
#define ST7789V2_RAMACC_VREFR_INV		(0x1 << 4)
#define ST7789V2_RAMACC_RGB_ORDER		(0x0 << 3)
